import Foundation
import UIKit

public extension UIColor{
    
    open class var primaryYellow: UIColor {
        return UIColor(red: 255/255, green: 215/255, blue: 64/255, alpha: 1.0)
    }
    
    open class var primaryGray: UIColor {
        return UIColor(red: 187/255, green: 187/255, blue: 187/255, alpha: 1.0)
    }
    
    open class var secondaryGray: UIColor {
        return UIColor(red: 237/255, green: 237/255, blue: 237/255, alpha: 1.0)
    }
    
    open class var primaryBrown: UIColor {
        return UIColor(red: 93/255, green: 64/255, blue: 55/255, alpha: 1.0)
    }
    
    open class var primaryGreen: UIColor {
        return UIColor(red: 121/255, green: 191/255, blue: 106/255, alpha: 1.0)
    }
    
    open class var primaryBlack: UIColor {
        return UIColor(red: 58/255, green: 60/255, blue: 62/255, alpha: 1.0)
    }
    
    open class var shadowColor: UIColor {
        return UIColor(red: 0, green: 0, blue: 0, alpha: 0.11)
    }
    
    open class var buttonShadowColor: UIColor {
        return UIColor(red: 64/255, green: 49/255, blue: 17/255, alpha: 0.3)
    }
    
    open class var underlineColor: UIColor {
        return UIColor(red: 153/255, green: 153/255, blue: 153/255, alpha: 1.0)
    }
    
    open class var yellowUnderlineColor: UIColor {
        return UIColor(red: 241/255, green: 201/255, blue: 52/255, alpha: 1.0)
    }
    
    open class var primaryRed: UIColor {
        return UIColor(red: 255/255, green: 107/255, blue: 109/255, alpha: 1.0)
    }
    
}
